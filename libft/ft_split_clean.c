#include "libft.h"

void	*ft_split_clean(char ***split)
{
	char	**s;

	s = *split;
	while (*s)
	{
		free(*s);
		*s = NULL;
		s++;
	}
	free(*split);
	*split = NULL;
	return (NULL);
}
